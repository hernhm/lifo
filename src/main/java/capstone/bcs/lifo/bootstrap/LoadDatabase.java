package capstone.bcs.lifo.bootstrap;

import capstone.bcs.lifo.model.Employee;

import capstone.bcs.lifo.repositories.EmployeeRespostiory;
import lombok.extern.slf4j.Slf4j;
import org.springframework.boot.CommandLineRunner;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;

@Configuration
@Slf4j
public class LoadDatabase {

    @Bean
    CommandLineRunner initDatabase(EmployeeRespostiory repository){
        return args -> {
            log.info("Preloading " + repository.save(new Employee("Billo Baggins","burglar")));
            log.info("Preloading" + repository.save(new Employee("Frodo Baggins","theif")));
        };
    }

}
